class Solution{
public:
void printSquare(int n) {
        //outer loop
        for(int i=0;i<n;i++){
            //inner loop
             for(int i=0;i<n;i++)
             {
                 cout<<"* ";
              } 
          cout<<endl;
        }
    }
};


